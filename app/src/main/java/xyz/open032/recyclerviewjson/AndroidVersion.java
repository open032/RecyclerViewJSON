package xyz.open032.recyclerviewjson;

public class AndroidVersion {
    private String id;
    private String description;
    private String time;
    private String rating;

    public String getVer() {
        return id;
    }

    public String getName() {
        return description;
    }

    public String getApi() {
        return time;
    }
    public String getRating() {
        return rating;
    }
}